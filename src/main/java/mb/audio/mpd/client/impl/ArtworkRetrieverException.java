package mb.audio.mpd.client.impl;

public class ArtworkRetrieverException extends Exception {
    private static final long serialVersionUID = 1L;

    public ArtworkRetrieverException() {
        super();
    }

    public ArtworkRetrieverException(String message, Throwable cause) {
        super(message, cause);
    }

    public ArtworkRetrieverException(String message) {
        super(message);
    }

    public ArtworkRetrieverException(Throwable cause) {
        super(cause);
    }

}
